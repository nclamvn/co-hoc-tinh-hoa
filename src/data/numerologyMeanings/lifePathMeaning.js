/**
 * Lấy thông tin của một số Đường Đời
 * @param {number} number - Số Đường Đời (1-9, 11, 22, 33)
 * @returns {Object|null} - Thông tin chi tiết
 */
export function getLifePathMeaning(number) {
  return lifePathMeanings[number] || null;
}

/**
 * Lấy danh sách tất cả các số Đường Đời
 * @returns {Array} - Mảng các object {number, name}
 */
export function getAllLifePathNumbers() {
  return Object.values(lifePathMeanings).map(lp => ({
    number: lp.number,
    name: lp.name,
    isMasterNumber: lp.isMasterNumber || false
  }));
}

/**
 * Lấy mô tả ngắn của một số Đường Đời
 * @param {number} number - Số Đường Đời
 * @returns {string} - Mô tả ngắn
 */
export function getLifePathShortDesc(number) {
  const lp = lifePathMeanings[number];
  return lp ? lp.overview.short : '';
}

/**
 * Kiểm tra xem có phải số Chủ không
 * @param {number} number - Số cần kiểm tra
 * @returns {boolean}
 */
export function isMasterNumber(number) {
  return [11, 22, 33].includes(number);
}

/**
 * Lấy số rút gọn của số Chủ
 * @param {number} masterNumber - Số Chủ (11, 22, 33)
 * @returns {number|null} - Số rút gọn
 */
export function getReducedNumber(masterNumber) {
  const lp = lifePathMeanings[masterNumber];
  return lp && lp.reducesTo ? lp.reducesTo : null;
}

/**
 * Lấy độ tương hợp giữa hai số Đường Đời
 * @param {number} number1 - Số Đường Đời thứ nhất
 * @param {number} number2 - Số Đường Đời thứ hai
 * @returns {string} - 'best', 'good', 'challenging', hoặc 'neutral'
 */
export function getCompatibility(number1, number2) {
  const lp = lifePathMeanings[number1];
  if (!lp || !lp.relationships || !lp.relationships.compatibility) {
    return 'neutral';
  }

  const compat = lp.relationships.compatibility;
  if (compat.best && compat.best.includes(number2)) return 'best';
  if (compat.good && compat.good.includes(number2)) return 'good';
  if (compat.challenging && compat.challenging.includes(number2)) return 'challenging';
  return 'neutral';
}

export default lifePathMeanings;

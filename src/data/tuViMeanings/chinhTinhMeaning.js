/**
 * Lấy thông tin của một chính tinh
 * @param {string} starId - ID của sao (tuVi, thienCo, thaiDuong, ...)
 * @returns {Object|null} - Thông tin chi tiết của sao
 */
export function getChinhTinhMeaning(starId) {
  return chinhTinhMeanings[starId] || null;
}

/**
 * Lấy danh sách tên các chính tinh
 * @returns {Array} - Mảng các object {id, name, altNames}
 */
export function getAllChinhTinhNames() {
  return Object.entries(chinhTinhMeanings).map(([id, star]) => ({
    id,
    name: star.name,
    altNames: star.altNames
  }));
}

/**
 * Lấy mô tả ngắn của một chính tinh
 * @param {string} starId - ID của sao
 * @returns {string} - Mô tả ngắn
 */
export function getChinhTinhShortDesc(starId) {
  const star = chinhTinhMeanings[starId];
  return star ? star.overview.short : '';
}

/**
 * Lấy chính tinh theo ngũ hành
 * @param {string} element - Ngũ hành (Kim, Mộc, Thủy, Hỏa, Thổ)
 * @returns {Array} - Danh sách các sao thuộc ngũ hành đó
 */
export function getChinhTinhByElement(element) {
  return Object.entries(chinhTinhMeanings)
    .filter(([_, star]) => star.element.includes(element))
    .map(([id, star]) => ({
      id,
      name: star.name,
      element: star.element
    }));
}

/**
 * Lấy chính tinh theo category
 * @param {string} category - Bắc Đẩu, Nam Đẩu, Trung Thiên
 * @returns {Array} - Danh sách các sao thuộc category đó
 */
export function getChinhTinhByCategory(category) {
  return Object.entries(chinhTinhMeanings)
    .filter(([_, star]) => star.category.includes(category))
    .map(([id, star]) => ({
      id,
      name: star.name,
      category: star.category
    }));
}

export default chinhTinhMeanings;

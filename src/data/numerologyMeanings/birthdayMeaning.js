export function getBirthdayMeaning(day) {
  return birthdayMeanings[day] || null;
}

export function getBirthdayShortDesc(day) {
  const b = birthdayMeanings[day];
  return b ? b.overview : '';
}

export function isBirthdayMasterNumber(day) {
  const b = birthdayMeanings[day];
  return b ? b.isMasterNumber || false : false;
}

export function isBirthdayKarmicDebt(day) {
  const b = birthdayMeanings[day];
  return b ? b.isKarmicDebt || false : false;
}

export default birthdayMeanings;

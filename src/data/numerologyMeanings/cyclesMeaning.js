/**
 * Tính Pinnacle numbers từ ngày sinh
 * @param {number} day - Ngày sinh
 * @param {number} month - Tháng sinh
 * @param {number} year - Năm sinh
 * @returns {Object} - 4 Pinnacle numbers và thời kỳ của chúng
 */
export function calculatePinnacles(day, month, year) {
  // Helper để giảm về single digit hoặc master number
  const reduce = (num) => {
    while (num > 9 && num !== 11 && num !== 22) {
      num = String(num).split('').reduce((sum, d) => sum + parseInt(d), 0);
    }
    return num;
  };

  // Reduce day, month, year
  const reducedDay = reduce(day);
  const reducedMonth = reduce(month);
  const reducedYear = reduce(String(year).split('').reduce((sum, d) => sum + parseInt(d), 0));

  // Calculate Life Path for timing
  const lifePath = reduce(reducedDay + reducedMonth + reducedYear);

  // Pinnacle calculations
  const pinnacle1 = reduce(reducedDay + reducedMonth);
  const pinnacle2 = reduce(reducedDay + reducedYear);
  const pinnacle3 = reduce(pinnacle1 + pinnacle2);
  const pinnacle4 = reduce(reducedMonth + reducedYear);

  // Calculate timing (36 - Life Path for first pinnacle end)
  const firstPinnacleEnd = 36 - lifePath;

  return {
    pinnacles: [pinnacle1, pinnacle2, pinnacle3, pinnacle4],
    periods: [
      { pinnacle: pinnacle1, from: 0, to: firstPinnacleEnd },
      { pinnacle: pinnacle2, from: firstPinnacleEnd + 1, to: firstPinnacleEnd + 9 },
      { pinnacle: pinnacle3, from: firstPinnacleEnd + 10, to: firstPinnacleEnd + 18 },
      { pinnacle: pinnacle4, from: firstPinnacleEnd + 19, to: 'lifetime' }
    ]
  };
}

/**
 * Tính Challenge numbers từ ngày sinh
 * @param {number} day - Ngày sinh
 * @param {number} month - Tháng sinh
 * @param {number} year - Năm sinh
 * @returns {Object} - 4 Challenge numbers
 */
export function calculateChallenges(day, month, year) {
  // Helper để giảm về single digit
  const reduce = (num) => {
    while (num > 9) {
      num = String(num).split('').reduce((sum, d) => sum + parseInt(d), 0);
    }
    return num;
  };

  // Reduce day, month, year
  const reducedDay = reduce(day);
  const reducedMonth = reduce(month);
  const reducedYear = reduce(String(year).split('').reduce((sum, d) => sum + parseInt(d), 0));

  // Challenge calculations (absolute differences)
  const challenge1 = Math.abs(reducedDay - reducedMonth);
  const challenge2 = Math.abs(reducedDay - reducedYear);
  const challenge3 = Math.abs(challenge1 - challenge2);
  const challenge4 = Math.abs(reducedMonth - reducedYear);

  return {
    challenges: [challenge1, challenge2, challenge3, challenge4]
  };
}

/**
 * Lấy Pinnacle hiện tại dựa trên tuổi
 * @param {number} age - Tuổi hiện tại
 * @param {Object} pinnacleData - Dữ liệu từ calculatePinnacles
 * @returns {Object} - Pinnacle hiện tại và thông tin
 */
export function getCurrentPinnacle(age, pinnacleData) {
  const { periods } = pinnacleData;

  for (let i = 0; i < periods.length; i++) {
    const period = periods[i];
    if (period.to === 'lifetime' || age <= period.to) {
      return {
        pinnacleNumber: period.pinnacle,
        periodIndex: i + 1,
        from: period.from,
        to: period.to,
        meaning: getPinnacleMeaning(period.pinnacle)
      };
    }
  }

  return null;
}


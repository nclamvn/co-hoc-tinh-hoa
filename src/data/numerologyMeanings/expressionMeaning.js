export function getExpressionMeaning(number) {
  return expressionMeanings[number] || null;
}

export function getAllExpressionNumbers() {
  return Object.values(expressionMeanings).map(e => ({
    number: e.number,
    name: e.name,
    isMasterNumber: e.isMasterNumber || false
  }));
}

export function getExpressionShortDesc(number) {
  const e = expressionMeanings[number];
  return e ? e.overview.short : '';
}

export function getExpressionCareers(number) {
  const e = expressionMeanings[number];
  return e ? e.bestCareers : [];
}

